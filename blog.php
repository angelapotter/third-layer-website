          <p>We had some setbacks in naming our company, mainly due to a refusal to take ourselves seriously combined with an unhealthy inclination toward bad wordplay.</p>
          <p>So we finally came up with a name that seems to strike an acceptable balance. It’s Third Layer.</p>
          <p>Sounds classy and even a little deep, right? Well we’re happy to report that the story behind it isn’t at all. In fact it’s very silly.</p>
          <p>Harry and I really like to make layered chip dip, and our specialty is an eight-layer Mexican dip. The eight layers are (in order from bottom to top): cream cheese, beans, avocado pieces, black olives, meat, salsa, cheddar cheese, and sour cream. This dip is delicious and easy to make, and has become a routine creation for us.</p>
          <p>In case you’re super curious about our eating habits (and I can’t imagine why you wouldn’t be), the Mexican dip originated with our love for nachos. Nachos are delicious, BUT there is one big problem: the chips get soggy, and pretty soon we’re left picking up handfuls of nacho mush and wishing we weren’t too proud to use a fork.</p>
          <p>Are we seriously the only ones who’ve noticed this fatal flaw? Dip is clearly superior to nachos.</p>
          <p>Anyway. The important part of this story is that the third layer of the dip is avocado pieces, which is by far the best ingredient in the whole thing.  We’ve played around with adding and subtracting other ingredients, but we would never mess with the avocados. The third layer is critical for the success of the dish as a whole.</p>
          <p>So that’s the story. We thought "third layer" sounded better than "avocado chunks."</p>
